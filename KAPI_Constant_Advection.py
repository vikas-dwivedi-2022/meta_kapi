def evaluate_exact_solution(x, t, x0, nu, speed=1.0):
    """
    Exact solution for periodic linear advection:
        u_t + speed*u_x = 0
    Here the true speed is 1.
    """
    s = np.remainder(x - speed * t, 1.0)
    return periodic_gaussian(s, x0, nu)

def plot_solution_snapshot(model, x0, nu, t_snap=0.5, nx=400, save_path=None):
    model.eval()

    x = np.linspace(0.0, 1.0, nx, endpoint=False)
    t = t_snap * np.ones_like(x)

    xt = np.stack([x, t], axis=1)[None, :, :]
    p = np.array([[x0, nu]], dtype=np.float32)

    xt_torch = torch.tensor(xt, dtype=torch.float32, device=device)
    p_torch = torch.tensor(p, dtype=torch.float32, device=device)

    with torch.no_grad():
        u_pred, (_, _, _, _, v_pred) = model(p_torch, xt_torch)

    u_pred = u_pred[0].cpu().numpy()
    v_pred = float(v_pred.item())
    u_exact = evaluate_exact_solution(x, t, x0, nu)

    plt.figure(figsize=(7, 4))
    plt.plot(x, u_exact, lw=2, label="Exact")
    plt.plot(x, u_pred, "--", lw=2, label="Prediction")
    plt.xlabel("x")
    plt.ylabel("u(x,t)")
    plt.title(f"Snapshot at t={t_snap:.2f}, x0={x0:.3f}, nu={nu:.3f}, v_pred={v_pred:.4f}")
    plt.legend()
    plt.grid(True, alpha=0.3)
    plt.tight_layout()

    if save_path is not None:
        plt.savefig(save_path, dpi=300)
        plt.close()
    else:
        plt.show()

def evaluate_case_on_grid(model, x0_test, nu_test, Nx=200, Nt=200):
    """
    Evaluate one test case on a full (x,t) grid.
    """
    x = np.linspace(0.0, 1.0, Nx, endpoint=False)
    t = np.linspace(0.0, 1.0, Nt)
    X, T = np.meshgrid(x, t, indexing="ij")

    xt_grid = np.stack([X.ravel(), T.ravel()], axis=1)
    xt_grid_t = torch.tensor(xt_grid, dtype=torch.float32, device=device).view(1, -1, 2)

    p_test = torch.tensor([[x0_test, nu_test]], dtype=torch.float32, device=device)

    model.eval()
    with torch.no_grad():
        u_pred_flat, (_, _, _, _, v_pred) = model(p_test, xt_grid_t)

    u_pred = u_pred_flat.cpu().numpy().reshape(Nx, Nt)
    v_pred_scalar = float(v_pred.item())

    u_exact = evaluate_exact_solution(X, T, x0_test, nu_test, speed=1.0)
    abs_err = np.abs(u_pred - u_exact)
    rel_l2 = np.linalg.norm((u_pred - u_exact).ravel()) / (np.linalg.norm(u_exact.ravel()) + 1e-14)

    return {
        "x": x,
        "t": t,
        "X": X,
        "T": T,
        "u_exact": u_exact,
        "u_pred": u_pred,
        "abs_err": abs_err,
        "rel_l2": rel_l2,
        "v_pred": v_pred_scalar,
    }

def plot_multicase_fields(model, test_cases, Nx=200, Nt=200, save_dir=FIG_DIR):
    """
    Combined multi-case figure in the style:
        columns = test cases
        rows    = [Exact, KAPI, |u_KAPI-u_Exact|]

    Titles only show (x0, nu).
    """
    num_tests = len(test_cases)

    fig, axs = plt.subplots(
        3, num_tests,
        figsize=(4.4 * num_tests, 9.6),
        constrained_layout=True
    )

    if num_tests == 1:
        axs = axs.reshape(3, 1)

    summary = []
    exact_list = []
    pred_list = []
    err_list = []

    for i, (name, x0_test, nu_test) in enumerate(test_cases):
        result = evaluate_case_on_grid(model, x0_test, nu_test, Nx=Nx, Nt=Nt)

        u_exact = result["u_exact"]
        u_pred  = result["u_pred"]
        abs_err = result["abs_err"]
        rel_l2  = result["rel_l2"]
        v_pred  = result["v_pred"]

        summary.append({
            "name": name,
            "x0": x0_test,
            "nu": nu_test,
            "rel_l2": rel_l2,
            "v_pred": v_pred,
            "speed_error": abs(v_pred - 1.0),
        })

        exact_list.append((x0_test, nu_test, u_exact))
        pred_list.append((x0_test, nu_test, u_pred))
        err_list.append((x0_test, nu_test, abs_err))

    # Shared color limits
    all_sol_vals = np.concatenate([u.ravel() for _, _, u in exact_list + pred_list])
    sol_vmin = np.min(all_sol_vals)
    sol_vmax = np.max(all_sol_vals)

    all_err_vals = np.concatenate([u.ravel() for _, _, u in err_list])
    err_vmin = 0.0
    err_vmax = np.max(all_err_vals)

    im_sol = None
    im_err = None

    for j in range(num_tests):

        x0_test, nu_test, u_exact = exact_list[j]
        _, _, u_pred = pred_list[j]
        _, _, abs_err = err_list[j]

        # Row 1: Exact
        ax = axs[0, j]
        im_sol = ax.imshow(
            u_exact.T,
            origin="lower",
            extent=[0, 1, 0, 1],
            aspect="auto",
            vmin=sol_vmin,
            vmax=sol_vmax
        )
        ax.set_title(rf"$(x_0,\nu)=({x0_test:.2f},{nu_test:.2f})$", fontsize=14)
        ax.set_xlabel("x")
        if j == 0:
            ax.set_ylabel("Exact", fontsize=13)

        # Row 2: Prediction
        ax = axs[1, j]
        ax.imshow(
            u_pred.T,
            origin="lower",
            extent=[0, 1, 0, 1],
            aspect="auto",
            vmin=sol_vmin,
            vmax=sol_vmax
        )
        ax.set_xlabel("x")
        if j == 0:
            ax.set_ylabel("KAPI", fontsize=13)

        # Row 3: Error
        ax = axs[2, j]
        im_err = ax.imshow(
            abs_err.T,
            origin="lower",
            extent=[0, 1, 0, 1],
            aspect="auto",
            vmin=err_vmin,
            vmax=err_vmax
        )
        ax.set_xlabel("x")
        if j == 0:
            ax.set_ylabel(r"$|u_{\mathrm{KAPI}}-u_{\mathrm{Exact}}|$", fontsize=13)

    # Shared colorbars
    cbar1 = fig.colorbar(im_sol, ax=axs[0:2, :], shrink=0.95, location="right")
    cbar1.set_label("Solution value", fontsize=12)

    cbar2 = fig.colorbar(im_err, ax=axs[2, :], shrink=0.95, location="right")
    cbar2.set_label("Absolute error", fontsize=12)

    # Training range annotation
    training_text = (
        rf"Training ranges: "
        rf"$x_0 \in [0.20,0.80]$, "
        rf"$\nu \in [{nu_min:.2f},{nu_max:.2f}]$"
    )

    fig.text(
        0.5, -0.02,
        training_text,
        ha="center",
        va="top",
        fontsize=13
    )

    combined_path = os.path.join(save_dir, "multicase_fields.png")
    fig.savefig(combined_path, dpi=300, bbox_inches="tight")
    plt.close(fig)

    print(f"Saved combined multicase field plot -> {combined_path}")

    return summary

def plot_time_slices(model, test_cases, t_slices=(0.0, 0.25, 0.50, 0.75, 1.0), Nx=400, save_dir=FIG_DIR):
    """
    For each test case, compare exact and predicted solution at multiple time slices.
    """
    x = np.linspace(0.0, 1.0, Nx, endpoint=False)

    for name, x0_test, nu_test in test_cases:
        fig, axs = plt.subplots(1, len(t_slices), figsize=(4 * len(t_slices), 3.8), constrained_layout=True)
        if len(t_slices) == 1:
            axs = [axs]

        p_test = torch.tensor([[x0_test, nu_test]], dtype=torch.float32, device=device)

        model.eval()
        learned_v = None

        for k, t_val in enumerate(t_slices):
            xt = np.stack([x, t_val * np.ones_like(x)], axis=1)[None, :, :]
            xt_t = torch.tensor(xt, dtype=torch.float32, device=device)

            with torch.no_grad():
                u_pred, (_, _, _, _, v_pred) = model(p_test, xt_t)

            u_pred = u_pred[0].cpu().numpy()
            u_exact = evaluate_exact_solution(x, t_val, x0_test, nu_test, speed=1.0)

            if learned_v is None:
                learned_v = float(v_pred.item())

            ax = axs[k]
            ax.plot(x, u_exact, lw=2, label="Exact")
            ax.plot(x, u_pred, "--", lw=2, label="Prediction")
            ax.set_title(f"t = {t_val:.2f}")
            ax.set_xlabel("x")
            ax.set_ylabel("u")
            ax.grid(True, alpha=0.3)
            if k == 0:
                ax.legend()

        fig.suptitle(
            f"Time-slice comparison: {name} | x0={x0_test:.3f}, nu={nu_test:.3f}, v_pred={learned_v:.4f}",
            fontsize=13
        )

        save_path = os.path.join(save_dir, f"time_slices_{name}.png")
        fig.savefig(save_path, dpi=300)
        plt.close(fig)
        print(f"Saved time-slice figure -> {save_path}")


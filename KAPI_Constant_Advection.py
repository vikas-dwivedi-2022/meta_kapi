def visualize_rbf_centers_xt_learned_speed(model, test_cases, K=60, save_dir=FIG_DIR):
    """
    For the learned-characteristic model:
        s = (x - v(p)t) mod 1
    each center s_j lifts to
        x = (v(p)t + s_j) mod 1
    """
    model.eval()
    t_vals = np.linspace(0.0, 1.0, K)

    for name, x0_test, nu_test in test_cases:
        p_test = torch.tensor([[x0_test, nu_test]], dtype=torch.float32, device=device)
        xt_dummy = torch.zeros(1, 1, 2, dtype=torch.float32, device=device)

        with torch.no_grad():
            _, (g, s_centers, sigma, coeff, v_pred) = model(p_test, xt_dummy)

        g_np = g.cpu().numpy().reshape(-1)
        s_np = s_centers.cpu().numpy().reshape(-1)
        coeff_np = coeff.cpu().numpy().reshape(-1)
        sigma_np = sigma.cpu().numpy().reshape(-1)
        v_scalar = float(v_pred.item())

        weights = np.abs(g_np * coeff_np) + 1e-12

        X_pts, T_pts, W_pts, S_pts = [], [], [], []

        for s_j, w_j, sig_j in zip(s_np, weights, sigma_np):
            for t_k in t_vals:
                x_k = (v_scalar * t_k + s_j) % 1.0
                X_pts.append(x_k)
                T_pts.append(t_k)
                W_pts.append(w_j)
                S_pts.append(150.0 * sig_j)

        X_pts = np.array(X_pts)
        T_pts = np.array(T_pts)
        W_pts = np.array(W_pts)
        S_pts = np.array(S_pts)

        fig, ax = plt.subplots(figsize=(5.5, 5.2))
        sc = ax.scatter(X_pts, T_pts, c=W_pts, s=np.clip(S_pts, 8.0, 80.0), cmap="viridis", alpha=0.85)
        plt.colorbar(sc, ax=ax, label="|g_j c_j|")

        ax.scatter([x0_test], [0.0], s=90, marker="x", color="red", label="IC center")
        ax.set_xlim(0, 1)
        ax.set_ylim(0, 1)
        ax.set_xlabel("x")
        ax.set_ylabel("t")
        ax.set_title(f"Learned Characteristic Lift\n{name}, v_pred={v_scalar:.4f}")
        ax.legend(loc="upper right")

        save_path = os.path.join(save_dir, f"rbf_centers_xt_{name}.png")
        plt.tight_layout()
        plt.savefig(save_path, dpi=300)
        plt.close(fig)

        print(f"Saved RBF-characteristic plot -> {save_path}")

def run_full_meta_evaluation(model, history, test_cases, Nx=200, Nt=200, save_dir=FIG_DIR):
    plot_training_history(history, os.path.join(save_dir, "training_loss.png"))

    summary = plot_multicase_fields(model, test_cases, Nx=Nx, Nt=Nt, save_dir=save_dir)

    plot_time_slices(
        model,
        test_cases,
        t_slices=(0.0, 0.25, 0.50, 0.75, 1.0),
        Nx=400,
        save_dir=save_dir
    )

    plot_error_summary(summary, save_dir=save_dir)
    plot_speed_summary(summary, save_dir=save_dir)
    save_summary_csv(summary, save_dir=save_dir)
    visualize_rbf_centers_xt_learned_speed(model, test_cases, K=60, save_dir=save_dir)

    print("\nSummary of test cases:")
    for row in summary:
        print(
            f"{row['name']:22s} | "
            f"x0={row['x0']:.3f}, nu={row['nu']:.3f} | "
            f"relL2={row['rel_l2']:.3e} | "
            f"v_pred={row['v_pred']:.4f} | "
            f"|v-1|={row['speed_error']:.3e}"
        )

    return summary


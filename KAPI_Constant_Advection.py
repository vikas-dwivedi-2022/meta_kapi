# PDE residual
# ============================================================

def advection_residual_batch(model, p, xt_int):
    """
    Computes PDE residual for u_t + u_x = 0 in batched form.

    Inputs:
        p      : (B,2)
        xt_int : (B,N,2)

    Returns:
        residual : (B,N)
    """
    xt_int = xt_int.clone().detach().requires_grad_(True)

    u, _ = model(p, xt_int)   # (B,N)

    grads = torch.autograd.grad(
        outputs=u,
        inputs=xt_int,
        grad_outputs=torch.ones_like(u),
        create_graph=True,
        retain_graph=True
    )[0]  # (B,N,2)

    u_x = grads[:, :, 0]
    u_t = grads[:, :, 1]

    return u_t + u_x


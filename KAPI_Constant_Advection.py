# ============================================================
# Periodic Gaussian
# ============================================================

def periodic_gaussian(z, mu, nu):
    """
    Periodic Gaussian on [0,1).
    Supports both torch tensors and numpy arrays.
    """
    if isinstance(z, torch.Tensor):
        d = torch.remainder(z - mu + 0.5, 1.0) - 0.5
        return torch.exp(-(d ** 2) / (2.0 * nu ** 2))
    else:
        d = np.remainder(z - mu + 0.5, 1.0) - 0.5
        return np.exp(-(d ** 2) / (2.0 * nu ** 2))


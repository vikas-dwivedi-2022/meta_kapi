def sample_p_batch(B, x0_range=(0.2, 0.8), nu_range=(0.03, 0.12)):
    """
    Sample parameter batch:
        p = [x0, nu]
    with log-uniform sampling in nu.
    """
    x0 = x0_range[0] + (x0_range[1] - x0_range[0]) * torch.rand(B, 1)

    u_rand = torch.rand(B, 1)

    nu_min_loc, nu_max_loc = nu_range
    log_nu_min = np.log10(nu_min_loc)
    log_nu_max = np.log10(nu_max_loc)

    nu = 10 ** (log_nu_min + (log_nu_max - log_nu_min) * u_rand)

    return torch.cat([x0, nu], dim=1).to(device)

def sample_interior_points_batch(B, N):
    x = torch.rand(B, N, 1)
    t = torch.rand(B, N, 1)
    return torch.cat([x, t], dim=2).to(device)

def sample_ic_points_batch(B, N):
    x = torch.rand(B, N, 1)
    t = torch.zeros(B, N, 1)
    return torch.cat([x, t], dim=2).to(device)

def sample_periodic_points_batch(B, N):
    t = torch.rand(B, N, 1)

    xL = torch.zeros(B, N, 1)
    xR = torch.ones(B, N, 1)

    xtL = torch.cat([xL, t], dim=2).to(device)
    xtR = torch.cat([xR, t], dim=2).to(device)

    return xtL, xtR


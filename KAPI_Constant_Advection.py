# Learned-characteristic Meta-KAPI-RBF
# ============================================================

class MetaKAPIRBF_Advection(nn.Module):
    def __init__(self, M=32, hidden_meta=64, hidden_speed=32):
        super().__init__()

        self.M = M

        # Meta-network producing RBF parameters
        self.meta = nn.Sequential(
            nn.Linear(2, hidden_meta),
            nn.Tanh(),
            nn.Linear(hidden_meta, hidden_meta),
            nn.Tanh(),
            nn.Linear(hidden_meta, 4 * M)
        )

        # Small network for characteristic speed
        self.speed_net = nn.Sequential(
            nn.Linear(2, hidden_speed),
            nn.Tanh(),
            nn.Linear(hidden_speed, hidden_speed),
            nn.Tanh(),
            nn.Linear(hidden_speed, 1)
        )

    def forward(self, p, xt):
        """
        Inputs:
            p  : (B, 2)      -> parameters [x0, nu]
            xt : (B, N, 2)   -> collocation/query points [x, t]

        Outputs:
            u  : (B, N)
            aux: tuple (g, s_centers, sigma, coeff, v)
        """
        B, N, _ = xt.shape

        # -----------------------------------
        # Normalize parameters
        # -----------------------------------
        p_mean = p.new_tensor([0.5, 0.075])
        p_std  = p.new_tensor([0.3, 0.03])
        p_norm = (p - p_mean) / p_std

        # -----------------------------------
        # Learned characteristic speed
        # -----------------------------------
        v = self.speed_net(p_norm)            # (B,1)
        v = 1.0 + 0.2 * torch.tanh(v)         # keep speed near 1

        # -----------------------------------
        # Meta-network
        # -----------------------------------
        meta_out = self.meta(p_norm).view(B, 4, self.M)

        g_logits  = meta_out[:, 0, :]
        s_raw     = meta_out[:, 1, :]
        log_sigma = meta_out[:, 2, :]
        coeff_raw = meta_out[:, 3, :]

        g = torch.sigmoid(g_logits)                             # (B,M)
        s_centers = torch.sigmoid(s_raw)                        # (B,M)
        sigma = torch.nn.functional.softplus(log_sigma) + 1e-3 # (B,M)
        coeff = torch.tanh(coeff_raw)                           # (B,M)

        # -----------------------------------
        # Characteristic coordinate
        # -----------------------------------
        x = xt[:, :, 0]   # (B,N)
        t = xt[:, :, 1]   # (B,N)

        s = torch.remainder(x - v * t, 1.0)  # (B,N)

        # -----------------------------------
        # Periodic RBF evaluation
        # -----------------------------------
        s_exp = s.unsqueeze(-1)               # (B,N,1)
        s_cent_exp = s_centers.unsqueeze(1)   # (B,1,M)
        sigma_exp = sigma.unsqueeze(1)        # (B,1,M)
        g_exp = g.unsqueeze(1)                # (B,1,M)
        coeff_exp = coeff.unsqueeze(1)        # (B,1,M)

        d = torch.remainder(s_exp - s_cent_exp + 0.5, 1.0) - 0.5
        r2 = d ** 2 / (sigma_exp ** 2)

        Phi = torch.exp(-r2)                  # (B,N,M)
        u = torch.sum(Phi * (g_exp * coeff_exp), dim=-1)  # (B,N)

        return u, (g, s_centers, sigma, coeff, v)


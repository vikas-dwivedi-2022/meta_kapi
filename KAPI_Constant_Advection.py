def train_meta_advection(
    B_pde=4,
    N_int=256,
    N_ic=128,
    N_per=128,
    epochs=5000,
    lr=1e-3,
    print_every=100
):
    model = MetaKAPIRBF_Advection(M=32, hidden_meta=64, hidden_speed=32).to(device)
    optimizer = optim.Adam(model.parameters(), lr=lr)

    best_loss = float("inf")
    best_state = None

    history = {
        "epoch": [],
        "loss": [],
        "loss_pde": [],
        "loss_ic": [],
        "loss_per": [],
        "loss_sparse": [],
        "loss_speed": []
    }

    for ep in range(1, epochs + 1):
        model.train()
        optimizer.zero_grad()

        # Curriculum in nu
        if ep <= epochs // 2:
            nu_min_curr, nu_max_curr = nu_easy, nu_max
        else:
            nu_min_curr, nu_max_curr = nu_min, nu_max

        # Sample batch
        p_batch = sample_p_batch(B_pde, nu_range=(nu_min_curr, nu_max_curr))
        xt_int = sample_interior_points_batch(B_pde, N_int)
        xt_ic = sample_ic_points_batch(B_pde, N_ic)
        xtL, xtR = sample_periodic_points_batch(B_pde, N_per)

        # PDE loss
        res = advection_residual_batch(model, p_batch, xt_int)
        loss_pde = torch.mean(res ** 2)

        # Initial condition loss
        u_ic, _ = model(p_batch, xt_ic)
        x_ic = xt_ic[:, :, 0]
        x0 = p_batch[:, 0:1].expand_as(x_ic)
        nu = p_batch[:, 1:2].expand_as(x_ic)
        u_ic_true = periodic_gaussian(x_ic, x0, nu)
        loss_ic = torch.mean((u_ic - u_ic_true) ** 2)

        # Periodic boundary loss
        uL, _ = model(p_batch, xtL)
        uR, _ = model(p_batch, xtR)
        loss_per = torch.mean((uL - uR) ** 2)

        # Sparsity on gate amplitudes
        xt_aux = sample_interior_points_batch(B_pde, 8)
        _, (g, _, _, _, _) = model(p_batch, xt_aux)
        loss_sparse = 1e-4 * torch.mean(torch.abs(g))

        # Speed regularization
        xt_speed = sample_ic_points_batch(B_pde, 1)
        _, (_, _, _, _, v) = model(p_batch, xt_speed)
        loss_speed = 1e-3 * torch.mean((v - 1.0) ** 2)

        # Total loss
        loss = loss_pde + loss_ic + loss_per + loss_sparse + loss_speed

        loss.backward()
        optimizer.step()

        history["epoch"].append(ep)
        history["loss"].append(loss.item())
        history["loss_pde"].append(loss_pde.item())
        history["loss_ic"].append(loss_ic.item())
        history["loss_per"].append(loss_per.item())
        history["loss_sparse"].append(loss_sparse.item())
        history["loss_speed"].append(loss_speed.item())

        if loss.item() < best_loss:
            best_loss = loss.item()
            best_state = {k: v.detach().cpu().clone() for k, v in model.state_dict().items()}

        if ep % print_every == 0 or ep == 1:
            print(
                f"Epoch {ep:4d} | "
                f"Loss {loss.item():.3e} | "
                f"PDE {loss_pde.item():.3e} | "
                f"IC {loss_ic.item():.3e} | "
                f"PER {loss_per.item():.3e} | "
                f"Speed {loss_speed.item():.3e}"
            )

        if device.type == "cuda":
            torch.cuda.empty_cache()

    if best_state is not None:
        model.load_state_dict(best_state)

    return model, history


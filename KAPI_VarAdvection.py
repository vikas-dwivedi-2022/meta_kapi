"""
Variable-speed periodic linear advection with learned characteristics

PDE:
    u_t + a(x;beta) u_x = 0,   x in [0,1], t in [0,1]
    a(x;beta) = 1 + beta*sin(2*pi*x),  |beta|<1

BC:
    u(0,t) = u(1,t)

IC:
    u(x,0) = u0(x; x0, nu)
where u0 is a periodic Gaussian bump.

Exact solution:
    Let F_beta(x) = int_0^x dxi / a(xi;beta)
    and P_beta = F_beta(1).
    Then the backward characteristic foot is
        s = F_beta^{-1}( (F_beta(x) - t) mod P_beta )
    and
        u(x,t) = u0(s; x0, nu)

This script trains a learned-characteristic meta-RBF model:
    s = (x - phi_theta(x,t,p)) mod 1,
    p = (x0, nu, beta)

and compares it to the exact solution above.
"""


    t = np.linspace(0.0, 1.0, Nt)
    X, T = np.meshgrid(x, t, indexing='ij')

    xt_grid = np.stack([X.ravel(), T.ravel()], axis=1)
    xt_grid_t = torch.tensor(xt_grid, dtype=torch.float32, device=device).view(1, -1, 2)

    p_test = torch.tensor([[x0_test, nu_test, beta_test]], dtype=torch.float32, device=device)

    with torch.no_grad():
        _, (_, _, _, _, phi, s) = model(p_test, xt_grid_t)

    phi_np = phi.view(Nx, Nt).cpu().numpy()
    s_np = s.view(Nx, Nt).cpu().numpy()

    plt.figure(figsize=(12, 4))

    plt.subplot(1, 2, 1)
    plt.imshow(phi_np.T, origin='lower', extent=[0, 1, 0, 1], aspect='auto')
    plt.xlabel("x")
    plt.ylabel("t")
    plt.title("Learned displacement phi(x,t,p)")
    plt.colorbar()

    plt.subplot(1, 2, 2)
    plt.imshow(s_np.T, origin='lower', extent=[0, 1, 0, 1], aspect='auto')
    plt.xlabel("x")
    plt.ylabel("t")
    plt.title("Learned characteristic coordinate s(x,t,p)")
    plt.colorbar()

    plt.tight_layout()
    path = os.path.join(FIG_DIR, "learned_map_diagnostic.png")

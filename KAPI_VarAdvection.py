# 7) Sampling utilities
# ============================================================

def sample_p_batch(B,
                   x0_range=x0_train_range,
                   nu_range=nu_train_range,
                   beta_range=beta_train_range):
    """
    Sample p=(x0,nu,beta).
    nu is sampled log-uniformly.
    """
    x0 = x0_range[0] + (x0_range[1] - x0_range[0]) * torch.rand(B, 1)

    u_rand = torch.rand(B, 1)
    nu_min, nu_max = nu_range
    nu = 10 ** (
        torch.log10(torch.tensor([nu_min])) +
        (torch.log10(torch.tensor([nu_max])) - torch.log10(torch.tensor([nu_min]))) * u_rand
    )

    beta = beta_range[0] + (beta_range[1] - beta_range[0]) * torch.rand(B, 1)

    return torch.cat([x0, nu, beta], dim=1).to(device)

def sample_interior_points_batch(B, N_int):
    x = torch.rand(B, N_int, 1)
    t = torch.rand(B, N_int, 1)
    return torch.cat([x, t], dim=2).to(device)

def sample_ic_points_batch(B, N_ic):
    x = torch.rand(B, N_ic, 1)
    t = torch.zeros(B, N_ic, 1)
    return torch.cat([x, t], dim=2).to(device)

def sample_periodic_points_batch(B, N_per):
    t = torch.rand(B, N_per, 1)
    xL = torch.zeros(B, N_per, 1)
    xR = torch.ones(B, N_per, 1)
    return torch.cat([xL, t], dim=2).to(device), torch.cat([xR, t], dim=2).to(device)

# ============================================================
# 8) PDE residual: u_t + a(x;beta) u_x
# ============================================================

def advection_residual_batch(model, p, xt_int):
    """
    p      : (B,3) = (x0,nu,beta)
    xt_int : (B,N,2)

    returns:
        residual : (B,N)
    """
    xt_req = xt_int.clone().detach().requires_grad_(True)

    u, _ = model(p, xt_req)

    grad_u = torch.autograd.grad(
        outputs=u,
        inputs=xt_req,
        grad_outputs=torch.ones_like(u),
        create_graph=True
    )[0]

    u_x = grad_u[:, :, 0]
    u_t = grad_u[:, :, 1]

    x = xt_req[:, :, 0]
    beta = p[:, 2:3].expand_as(x)

    a = a_of_x_torch(x, beta)

    return u_t + a * u_x

# ============================================================
# 9) Training
# ============================================================

def train_meta_varspeed_advection(
    B_pde=8,
    N_int=512,
    N_ic=256,
    N_per=256,
    epochs=5000,
    lr=5e-4,
    M=64,
    hidden_meta=64,
    hidden_char=64,
    w_sparse=1e-4,
    w_warp=1e-5
):
    model = MetaKAPIRBF_VarSpeedAdvection(
        M=M,
        hidden_meta=hidden_meta,
        hidden_char=hidden_char
    ).to(device)

    optimizer = optim.Adam(model.parameters(), lr=lr)

    best_loss = float("inf")
    best_state = None

    history = {
        "epoch": [],
        "total_loss": [],
        "loss_pde": [],
        "loss_ic": [],
        "loss_per": [],
        "loss_sparse": [],
        "loss_warp": []
    }

    for ep in range(1, epochs + 1):
        optimizer.zero_grad()

        # Curriculum on nu only
        if ep <= epochs // 2:
            nu_min_curr, nu_max_curr = nu_easy, nu_train_range[1]
        else:
            nu_min_curr, nu_max_curr = nu_train_range

        p_batch = sample_p_batch(
            B_pde,
            x0_range=x0_train_range,
            nu_range=(nu_min_curr, nu_max_curr),
            beta_range=beta_train_range
        )

        xt_int = sample_interior_points_batch(B_pde, N_int)
        xt_ic  = sample_ic_points_batch(B_pde, N_ic)
        xtL, xtR = sample_periodic_points_batch(B_pde, N_per)

        # PDE residual
        res_int = advection_residual_batch(model, p_batch, xt_int)
        loss_pde = torch.mean(res_int**2)

        # IC loss
        u_ic, _ = model(p_batch, xt_ic)
        x_ic = xt_ic[:, :, 0]
        x0 = p_batch[:, 0:1].expand_as(x_ic)
        nu = p_batch[:, 1:2].expand_as(x_ic)

        u_ic_exact = periodic_gaussian(x_ic, x0, nu)
        loss_ic = torch.mean((u_ic - u_ic_exact)**2)

        # Periodic BC
        uL, _ = model(p_batch, xtL)
        uR, _ = model(p_batch, xtR)
        loss_per = torch.mean((uL - uR)**2)

        # Sparsity on gates
        _, (g, _, _, _, _, _) = model(p_batch, sample_interior_points_batch(B_pde, 64))
        loss_sparse = w_sparse * torch.mean(torch.abs(g))

        # Mild warp regularization
        _, (_, _, _, _, phi_reg, _) = model(p_batch, sample_interior_points_batch(B_pde, 64))
        loss_warp = w_warp * torch.mean(phi_reg**2)

        loss = loss_pde + loss_ic + loss_per + loss_sparse + loss_warp

        loss.backward()
        optimizer.step()

        history["epoch"].append(ep)
        history["total_loss"].append(loss.item())
        history["loss_pde"].append(loss_pde.item())
        history["loss_ic"].append(loss_ic.item())
        history["loss_per"].append(loss_per.item())
        history["loss_sparse"].append(loss_sparse.item())
        history["loss_warp"].append(loss_warp.item())

        if loss.item() < best_loss:
            best_loss = loss.item()
            best_state = {k: v.detach().cpu().clone() for k, v in model.state_dict().items()}

        if ep % 200 == 0:
            print(
                f"Epoch {ep:4d} | Loss={loss.item():.3e} "
                f"| PDE={loss_pde.item():.3e} "
                f"| IC={loss_ic.item():.3e} "
                f"| PER={loss_per.item():.3e} "
                f"| nu-range=[{nu_min_curr:.3f},{nu_max_curr:.3f}]"
            )

    print(f"\nBest loss = {best_loss:.3e}")
    model.load_state_dict({k: v.to(device) for k, v in best_state.items()})

    return model, history


def evaluate_and_plot_varspeed(model, test_cases, Nx=220, Nt=220, map_grid=20001):
    """
    test_cases: list of tuples
        (name, x0_test, nu_test, beta_test)

    Layout:
        columns = test cases
        rows    = [Exact, KAPI, |u_KAPI-u_Exact|]
    """
    num_tests = len(test_cases)

    fig, axs = plt.subplots(
        3, num_tests,
        figsize=(4.2 * num_tests, 9.5),
        constrained_layout=True
    )

    if num_tests == 1:
        axs = axs.reshape(3, 1)

    rel_errors = []

    x = np.linspace(0.0, 1.0, Nx)
    t = np.linspace(0.0, 1.0, Nt)
    X, T = np.meshgrid(x, t, indexing='ij')

    xt_grid = np.stack([X.ravel(), T.ravel()], axis=1)
    xt_grid_t = torch.tensor(xt_grid, dtype=torch.float32, device=device).view(1, -1, 2)

    model.eval()

    exact_list = []
    pred_list = []
    err_list = []

    for i, (_, x0_test, nu_test, beta_test) in enumerate(test_cases):
        print(f"\nTest case {i+1}, p=({x0_test}, {nu_test}, {beta_test})")

        p_test = torch.tensor([[x0_test, nu_test, beta_test]], dtype=torch.float32, device=device)

        with torch.no_grad():
            pred_flat, _ = model(p_test, xt_grid_t)
        u_pred = pred_flat.cpu().numpy().reshape(Nx, Nt)

        u_exact = exact_solution_variable_speed(X, T, x0_test, nu_test, beta_test, n_grid_map=map_grid)
        u_err = np.abs(u_pred - u_exact)

        rel_L2 = np.linalg.norm((u_pred - u_exact).ravel()) / (np.linalg.norm(u_exact.ravel()) + 1e-12)
        rel_errors.append((i + 1, rel_L2))
        print(f"Relative L2 = {rel_L2:.3e}")

        exact_list.append((x0_test, nu_test, beta_test, u_exact))
        pred_list.append((x0_test, nu_test, beta_test, u_pred))
        err_list.append((x0_test, nu_test, beta_test, u_err))

    # Shared color limits
    all_sol_vals = np.concatenate([u.ravel() for _, _, _, u in exact_list + pred_list])
    sol_vmin = np.min(all_sol_vals)
    sol_vmax = np.max(all_sol_vals)

    all_err_vals = np.concatenate([u.ravel() for _, _, _, u in err_list])
    err_vmin = 0.0
    err_vmax = np.max(all_err_vals)

    im_sol = None
    im_err = None

    for j in range(num_tests):
        x0_test, nu_test, beta_test, u_exact = exact_list[j]
        _, _, _, u_pred = pred_list[j]
        _, _, _, u_err = err_list[j]

        # Row 1: Exact
        ax = axs[0, j]
        im_sol = ax.imshow(
            u_exact.T,
            origin='lower',
            extent=[0, 1, 0, 1],
            aspect='auto',
            vmin=sol_vmin,
            vmax=sol_vmax
        )
        ax.set_title(rf"$(x_0,\nu,\beta)=({x0_test:.2f},{nu_test:.2f},{beta_test:.2f})$", fontsize=14)
        ax.set_xlabel("x")
        if j == 0:
            ax.set_ylabel("Exact", fontsize=13)

        # Row 2: KAPI
        ax = axs[1, j]
        ax.imshow(
            u_pred.T,
            origin='lower',
            extent=[0, 1, 0, 1],
            aspect='auto',
            vmin=sol_vmin,
            vmax=sol_vmax
        )
        ax.set_xlabel("x")
        if j == 0:
            ax.set_ylabel("KAPI", fontsize=13)

        # Row 3: Error
        ax = axs[2, j]
        im_err = ax.imshow(
            u_err.T,
            origin='lower',
            extent=[0, 1, 0, 1],
            aspect='auto',
            vmin=err_vmin,
            vmax=err_vmax
        )
        ax.set_xlabel("x")
        if j == 0:
            ax.set_ylabel(r"$|u_{\mathrm{KAPI}}-u_{\mathrm{Exact}}|$", fontsize=13)

    # Two colorbars only
    cbar1 = fig.colorbar(im_sol, ax=axs[0:2, :], shrink=0.95, location='right')
    cbar1.set_label("Solution value", fontsize=12)

    cbar2 = fig.colorbar(im_err, ax=axs[2, :], shrink=0.95, location='right')
    cbar2.set_label("Absolute error", fontsize=12)

    # Training ranges at the bottom of the full figure
    training_text = (
        rf"Training ranges: "
        rf"$x_0 \in [{x0_train_range[0]:.2f},{x0_train_range[1]:.2f}]$, "
        rf"$\nu \in [{nu_train_range[0]:.2f},{nu_train_range[1]:.2f}]$, "
        rf"$\beta \in [{beta_train_range[0]:.2f},{beta_train_range[1]:.2f}]$"
    )

    fig.text(
        0.5, -0.02,
        training_text,
        ha='center',
        va='top',
        fontsize=13
    )

    combined_path = os.path.join(FIG_DIR, "varspeed_meta_results_clean.png")
    plt.savefig(combined_path, dpi=300, bbox_inches='tight')
    plt.show()
    print(f"\nSaved combined results -> {combined_path}")

    print("\nSummary of relative L2 errors:")
    for idx, e in rel_errors:
        print(f"  Test case {idx:2d}: {e:.3e}")


# ============================================================
# 3) Periodic Gaussian + periodic distance
# ============================================================

def periodic_distance_torch(x, y):
    return torch.remainder(x - y + 0.5, 1.0) - 0.5

def periodic_distance_np(x, y):
    return np.remainder(x - y + 0.5, 1.0) - 0.5

def periodic_gaussian(z, mu, nu):
    """
    Periodic wrapped Gaussian centered at mu with width nu.
    """
    if isinstance(z, torch.Tensor):
        d = periodic_distance_torch(z, mu)
        return torch.exp(-d**2 / (2.0 * nu**2))
    else:
        d = periodic_distance_np(z, mu)
        return np.exp(-d**2 / (2.0 * nu**2))

# ============================================================
# 4) Variable-speed field
# ============================================================

def a_of_x_torch(x, beta):
    return 1.0 + beta * torch.sin(2.0 * math.pi * x)

def a_of_x_np(x, beta):
    return 1.0 + beta * np.sin(2.0 * math.pi * x)

# ============================================================
# 5) Exact solution via characteristic travel-time map
# ============================================================

class TravelTimeMap:
    """
    Numerically exact characteristic map for
        dx/dt = a(x;beta) = 1 + beta*sin(2*pi*x),  |beta|<1

    We build:
        F_beta(x) = int_0^x 1/a(xi;beta) dxi
    on a dense grid, then invert by interpolation.

    Exact solution:
        s = F^{-1}( (F(x)-t) mod P )
        u(x,t) = u0(s)
    """
    def __init__(self, beta, n_grid=20001):
        self.beta = float(beta)
        self.n_grid = int(n_grid)

        x = np.linspace(0.0, 1.0, self.n_grid)
        a = a_of_x_np(x, self.beta)

        inv_a = 1.0 / a

        F = np.zeros_like(x)
        dx = x[1] - x[0]
        F[1:] = np.cumsum(0.5 * (inv_a[:-1] + inv_a[1:]) * dx)

        self.x = x
        self.F = F
        self.P = F[-1]

    def forward_F(self, xq):
        """
        F(xq), with xq wrapped into [0,1].
        """
        xq_wrapped = np.mod(xq, 1.0)
        return np.interp(xq_wrapped, self.x, self.F)

    def inverse_F(self, tauq):
        """
        F^{-1}(tauq), where tauq is wrapped into [0,P].
        """
        tau_wrapped = np.mod(tauq, self.P)
        return np.interp(tau_wrapped, self.F, self.x)

def exact_solution_variable_speed(X, T, x0, nu, beta, n_grid_map=20001):
    """
    Exact solution evaluated on arrays X,T of same shape.
    """
    mapper = TravelTimeMap(beta=beta, n_grid=n_grid_map)

    Fx = mapper.forward_F(X)
    tau0 = np.mod(Fx - T, mapper.P)
    s = mapper.inverse_F(tau0)

    return periodic_gaussian(s, x0, nu)

# ============================================================
# 6) Learned-characteristic Meta-RBF model
# ============================================================

class MetaKAPIRBF_VarSpeedAdvection(nn.Module):
    """
    p = (x0, nu, beta)

    Learned characteristic coordinate:
        s = (x - phi_theta(x,t,p)) mod 1

    Meta-network outputs RBF parameters in s-space:
        g_j, s_center_j, sigma_j, coeff_j

    RBF representation:
        u = sum_j (g_j * coeff_j) exp( - d_per(s,s_j)^2 / sigma_j^2 )
    """
    def __init__(self, M=64, hidden_meta=64, hidden_char=64):
        super().__init__()
        self.M = M

        # Meta-network for RBF dictionary parameters
        self.meta = nn.Sequential(
            nn.Linear(3, hidden_meta),
            nn.Tanh(),
            nn.Linear(hidden_meta, hidden_meta),
            nn.Tanh(),
            nn.Linear(hidden_meta, 4 * M)
        )

        # Characteristic map network
        # Inputs use periodic x-encoding: [sin(2pi x), cos(2pi x), t, normalized p]
        self.char_net = nn.Sequential(
            nn.Linear(2 + 1 + 3, hidden_char),  # sinx, cosx, t, x0,nu,beta
            nn.Tanh(),
            nn.Linear(hidden_char, hidden_char),
            nn.Tanh(),
            nn.Linear(hidden_char, 1)
        )

    def normalize_p(self, p):
        # p = (x0, nu, beta)
        p_mean = p.new_tensor([0.5, 0.075, 0.40])
        p_std  = p.new_tensor([0.3, 0.03, 0.15])
        return (p - p_mean) / p_std

    def forward(self, p, xt):
        """
        p  : (B,3)
        xt : (B,N,2)

        returns:
            u   : (B,N)
            aux : tuple for diagnostics
        """
        B, N, _ = xt.shape

        p_norm = self.normalize_p(p)

        # -----------------------------------
        # Learned characteristic displacement
        # -----------------------------------
        x = xt[:, :, 0]
        t = xt[:, :, 1]

        sinx = torch.sin(2.0 * math.pi * x)
        cosx = torch.cos(2.0 * math.pi * x)

        p_rep = p_norm.unsqueeze(1).expand(B, N, 3)

        char_in = torch.cat([
            sinx.unsqueeze(-1),
            cosx.unsqueeze(-1),
            t.unsqueeze(-1),
            p_rep
        ], dim=-1)

        phi_raw = self.char_net(char_in.reshape(B * N, -1)).reshape(B, N)

        # Enforce phi(x,0,p)=0 by multiplying by t
        # Bound displacement magnitude moderately for stability
        phi = t * (1.25 * torch.tanh(phi_raw))

        s = torch.remainder(x - phi, 1.0)

        # -----------------------------------
        # Meta-network for RBF parameters
        # -----------------------------------
        meta_out = self.meta(p_norm).view(B, 4, self.M)

        g_logits  = meta_out[:, 0, :]
        s_raw     = meta_out[:, 1, :]
        log_sigr  = meta_out[:, 2, :]
        coeff_raw = meta_out[:, 3, :]

        g = torch.sigmoid(g_logits)
        s_centers = torch.sigmoid(s_raw)
        sigma = torch.nn.functional.softplus(log_sigr) + 1e-3
        coeff = torch.tanh(coeff_raw)

        # -----------------------------------
        # RBF evaluation on periodic s-domain
        # -----------------------------------
        s_exp = s.unsqueeze(-1)                    # (B,N,1)
        s_cent_exp = s_centers.unsqueeze(1)       # (B,1,M)
        sigma_exp = sigma.unsqueeze(1)            # (B,1,M)
        g_exp = g.unsqueeze(1)                    # (B,1,M)
        coeff_exp = coeff.unsqueeze(1)            # (B,1,M)

        d = torch.remainder(s_exp - s_cent_exp + 0.5, 1.0) - 0.5
        r2 = d**2 / (sigma_exp**2)
        Phi = torch.exp(-r2)

        u = torch.sum(Phi * (g_exp * coeff_exp), dim=-1)

        return u, (g, s_centers, sigma, coeff, phi, s)


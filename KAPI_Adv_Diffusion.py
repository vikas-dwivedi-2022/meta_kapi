# ------------------------------
# Cell 2: Exact solution for advection–diffusion
#   u_t + a u_x = nu u_xx
#   u(x,t) = 1/sqrt(4t+1) * exp(-(x-0.2-a t)^2 / (nu(4t+1)))
# ------------------------------

def exact_advecdiff_torch(x, t, a, nu):
    """
    x, t, a, nu: torch tensors broadcastable to same shape
    returns u_exact as torch tensor
    """
    four_t1 = 4.0 * t + 1.0
    num = (x - 0.2 - a * t) ** 2
    den = nu * four_t1
    u = 1.0 / torch.sqrt(four_t1) * torch.exp(-num / den)
    return u

def exact_advecdiff_np(x, t, a, nu):
    """
    x, t: 2D meshgrid arrays (Nx, Nt)
    a, nu: scalars
    returns u_exact as (Nx, Nt) ndarray
    """
    four_t1 = 4.0 * t + 1.0
    num = (x - 0.2 - a * t) ** 2
    den = nu * four_t1
    u = 1.0 / np.sqrt(four_t1) * np.exp(-num / den)
    return u

# ------------------------------
# Cell 3: MetaDynamic KAPI–RBF model for advection–diffusion
# ------------------------------

class MetaDynamicKAPIRBF_AdvecDiff(nn.Module):
    def __init__(self, M=60):
        """
        M: number of RBF basis functions in x.
        Distributional parameters depend on (t,a,nu).
        """
        super().__init__()
        self.M = M

        # Gates g_j in (0,1) to prune or emphasize RBFs
        self.g_logits = nn.Parameter(torch.zeros(M))

        # Centers:
        # mu_j(t,a,nu) = tanh( mu0_j + vt_j t + va_j a + vnu_j nu )
        self.mu0   = nn.Parameter(torch.linspace(0.0, 1.0, M))
        self.v_t   = nn.Parameter(0.1 * torch.randn(M))
        self.v_a   = nn.Parameter(0.1 * torch.randn(M))
        self.v_nu  = nn.Parameter(0.1 * torch.randn(M))

        # Widths:
        # sigma_j(t,a,nu) = softplus(alpha0_j + st_j t + sa_j a + snu_j nu) + eps
        self.alpha0 = nn.Parameter(-1.0 * torch.ones(M))
        self.s_t    = nn.Parameter(0.05 * torch.randn(M))
        self.s_a    = nn.Parameter(0.05 * torch.randn(M))
        self.s_nu   = nn.Parameter(0.05 * torch.randn(M))

        # Amplitudes c_j (global across parameters)
        self.c = nn.Parameter(0.1 * torch.randn(M))

        self.eps = 1e-4

    def forward(self, xtanu):
        """
        xtanu: (N,4) tensor with columns (x,t,a,nu)
        Returns u(x,t,a,nu) as shape (N,)
        """
        x  = xtanu[:, 0:1]  # (N,1)
        t  = xtanu[:, 1:2]
        a  = xtanu[:, 2:3]
        nu = xtanu[:, 3:4]

        g = torch.sigmoid(self.g_logits)  # (M,)

        mu_tanu = torch.tanh(
            self.mu0.view(1, self.M)
            + self.v_t.view(1, self.M)  * t
            + self.v_a.view(1, self.M)  * a
            + self.v_nu.view(1, self.M) * nu
        )  # (N,M)

        sigma_tanu = torch.nn.functional.softplus(
            self.alpha0.view(1, self.M)
            + self.s_t.view(1, self.M)  * t
            + self.s_a.view(1, self.M)  * a
            + self.s_nu.view(1, self.M) * nu
        ) + self.eps  # (N,M)

        x_diff = x - mu_tanu
        r2 = (x_diff ** 2) / (2.0 * sigma_tanu ** 2)
        Phi = torch.exp(-r2)  # (N,M)

        coeff = g * self.c    # (M,)
        u = Phi @ coeff       # (N,)
        return u


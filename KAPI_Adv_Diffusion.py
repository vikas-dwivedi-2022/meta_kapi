def visualize_rbf_centers_xt_conv_diff(model, test_cases, K=50):
    """
    Visualize RBF centers mu_j(t,a,nu) in (x,t)-plane for each test (a,nu).
    For each center j and time sample t_k, we plot (mu_j(t_k), t_k),
    colored by |g_j * c_j|.
    """
    model.eval()

    for name, a_test, nu_test in test_cases:
        print(f"\nRBF center visualization in (x,t) for: {name}")

        # Time samples
        t_vals = torch.linspace(t_min, t_max, K, device=device).view(K, 1)
        a_vals = torch.full((K, 1), a_test, device=device)
        nu_vals = torch.full((K, 1), nu_test, device=device)
        x_dummy = torch.zeros(K, 1, device=device)  # not used in mu formula

        # We recompute mu_tanu exactly as in forward, but for multiple t
        with torch.no_grad():
            mu_tanu = torch.tanh(
                model.mu0.view(1, model.M)
                + model.v_t.view(1, model.M)  * t_vals
                + model.v_a.view(1, model.M)  * a_vals
                + model.v_nu.view(1, model.M) * nu_vals
            )  # (K, M)

            g = torch.sigmoid(model.g_logits)      # (M,)
            c = model.c                            # (M,)
            w = torch.abs(g * c) + 1e-12           # (M,)

        mu_np = mu_tanu.cpu().numpy()   # (K,M)
        t_np  = t_vals.cpu().numpy()    # (K,1)
        w_np  = w.cpu().numpy()         # (M,)

        # Build point cloud in (x,t)
        X_pts = []
        T_pts = []
        W_pts = []

        for j in range(model.M):
            for k in range(K):
                X_pts.append(mu_np[k, j])
                T_pts.append(t_np[k, 0])
                W_pts.append(w_np[j])

        X_pts = np.array(X_pts)
        T_pts = np.array(T_pts)
        W_pts = np.array(W_pts)

        fig, ax = plt.subplots(figsize=(5, 5))
        sc = ax.scatter(X_pts, T_pts, c=W_pts, s=12, cmap="viridis")
        plt.colorbar(sc, ax=ax, label="|g * c|")

        ax.set_title(f"RBF centers in (x,t)\n{name}", fontsize=12)
        ax.set_xlabel("x")
        ax.set_ylabel("t")
        ax.set_xlim(x_min - 0.1, x_max + 0.1)
        ax.set_ylim(t_min, t_max)

        # Mark initial Gaussian center at t=0, x=0.2
        ax.scatter([0.2], [0.0], color="red", marker="x", s=80, label="IC center")
        ax.legend(loc="upper right")

        centers_path = os.path.join(FIG_DIR, f"rbf_centers_xt_{name}.png")
        plt.tight_layout()
        plt.savefig(centers_path, dpi=300)
        plt.show()

        print(f"Saved RBF centers (x,t) figure → {centers_path}")


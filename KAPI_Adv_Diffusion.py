# Cell 8: Evaluate on multiple (a,nu) cases + plots + save figures
#         3-row, 4-column multicase_fields.png template
# ------------------------------

def evaluate_and_plot_conv_diff(model, test_cases, Nx=200, Nt=200):
    """
    test_cases: list of (name, a_test, nu_test)

    Produces:
      - combined figure with:
            Row 1: Exact
            Row 2: Meta
            Row 3: Absolute error
        and columns = test cases
      - per-case figures (same as before)
      - printed relative L2 summary
    """
    num_tests = len(test_cases)

    x = np.linspace(x_min, x_max, Nx)
    t = np.linspace(t_min, t_max, Nt)
    Xg, Tg = np.meshgrid(x, t, indexing="ij")

    model.eval()

    summary = []
    exact_list = []
    pred_list = []
    err_list = []

    # -----------------------------------
    # First pass: evaluate all test cases
    # -----------------------------------
    for i, (name, a_test, nu_test) in enumerate(test_cases):
        print(f"\n=== Test case {i+1}/{num_tests}: {name} ===")
        print(f"  (a, nu) = ({a_test:.3f}, {nu_test:.3f})")

        # Exact
        U_exact = exact_advecdiff_np(Xg, Tg, a_test, nu_test)

        # Grid for model
        A_grid  = a_test * np.ones_like(Xg)
        Nu_grid = nu_test * np.ones_like(Xg)

        xtanu_grid = np.stack(
            [Xg.reshape(-1), Tg.reshape(-1), A_grid.reshape(-1), Nu_grid.reshape(-1)],
            axis=1
        )
        xtanu_grid_t = torch.tensor(xtanu_grid, dtype=torch.float32, device=device)

        with torch.no_grad():
            u_meta_flat = model(xtanu_grid_t)

        U_meta = u_meta_flat.cpu().numpy().reshape(Nx, Nt)

        err = np.abs(U_meta - U_exact)
        rel_L2 = np.linalg.norm(err.ravel()) / (np.linalg.norm(U_exact.ravel()) + 1e-14)

        summary.append({
            "name": name,
            "a": a_test,
            "nu": nu_test,
            "rel_l2": rel_L2
        })

        exact_list.append((name, a_test, nu_test, U_exact))
        pred_list.append((name, a_test, nu_test, U_meta))
        err_list.append((name, a_test, nu_test, err))

        print(f"  Relative L2 error = {rel_L2:.3e}")

        # -----------------------------------
        # Per-case figure (kept as before)
        # -----------------------------------
        single_path = os.path.join(FIG_DIR, f"meta_conv_diff_{name}.png")

        plt.figure(figsize=(12, 4))

        plt.subplot(1, 3, 1)
        plt.imshow(U_exact.T, extent=[x_min, x_max, t_min, t_max],
                   origin="lower", aspect="auto")
        plt.title("Exact")
        plt.xlabel("x")
        plt.ylabel("t")
        plt.colorbar()

        plt.subplot(1, 3, 2)
        plt.imshow(U_meta.T, extent=[x_min, x_max, t_min, t_max],
                   origin="lower", aspect="auto")
        plt.title("Meta")
        plt.xlabel("x")
        plt.ylabel("t")
        plt.colorbar()

        plt.subplot(1, 3, 3)
        plt.imshow(err.T, extent=[x_min, x_max, t_min, t_max],
                   origin="lower", aspect="auto")
        plt.title("Error")
        plt.xlabel("x")
        plt.ylabel("t")
        plt.colorbar()

        plt.tight_layout()
        plt.savefig(single_path, dpi=300)
        plt.close()
        print(f"  Saved per-case figure → {single_path}")

    # -----------------------------------
    # Shared color limits for combined plot
    # -----------------------------------
    all_sol_vals = np.concatenate(
        [U.ravel() for _, _, _, U in exact_list + pred_list]
    )
    sol_vmin = np.min(all_sol_vals)
    sol_vmax = np.max(all_sol_vals)

    all_err_vals = np.concatenate(
        [E.ravel() for _, _, _, E in err_list]
    )
    err_vmin = 0.0
    err_vmax = np.max(all_err_vals)

    # -----------------------------------
    # Combined 3-row, num_tests-column plot
    # -----------------------------------
    fig, axs = plt.subplots(
        3, num_tests,
        figsize=(4.4 * num_tests, 9.6),
        constrained_layout=True
    )

    if num_tests == 1:
        axs = axs.reshape(3, 1)

    im_sol = None
    im_err = None

    for j in range(num_tests):
        name, a_test, nu_test, U_exact = exact_list[j]
        _, _, _, U_meta = pred_list[j]
        _, _, _, err = err_list[j]

        # Row 1: Exact
        ax = axs[0, j]
        im_sol = ax.imshow(
            U_exact.T,
            origin="lower",
            extent=[x_min, x_max, t_min, t_max],
            aspect="auto",
            vmin=sol_vmin,
            vmax=sol_vmax
        )
        ax.set_title(rf"$(a,\nu)=({a_test:.2f},{nu_test:.3f})$", fontsize=14)
        ax.set_xlabel("x")
        if j == 0:
            ax.set_ylabel("Exact", fontsize=13)

        # Row 2: Meta
        ax = axs[1, j]
        ax.imshow(
            U_meta.T,
            origin="lower",
            extent=[x_min, x_max, t_min, t_max],
            aspect="auto",
            vmin=sol_vmin,
            vmax=sol_vmax
        )
        ax.set_xlabel("x")
        if j == 0:
            ax.set_ylabel("KAPI", fontsize=13)

        # Row 3: Error
        ax = axs[2, j]
        im_err = ax.imshow(
            err.T,
            origin="lower",
            extent=[x_min, x_max, t_min, t_max],
            aspect="auto",
            vmin=err_vmin,
            vmax=err_vmax
        )
        ax.set_xlabel("x")
        if j == 0:
            ax.set_ylabel(r"$|u_{\mathrm{KAPI}}-u_{\mathrm{Exact}}|$", fontsize=13)

    # Shared colorbars
    cbar1 = fig.colorbar(im_sol, ax=axs[0:2, :], shrink=0.95, location="right")
    cbar1.set_label("Solution value", fontsize=12)

    cbar2 = fig.colorbar(im_err, ax=axs[2, :], shrink=0.95, location="right")
    cbar2.set_label("Absolute error", fontsize=12)

    # Training range annotation
    training_text = (
        rf"Training ranges: "
        rf"$a \in [{a_min:.2f},{a_max:.2f}]$, "
        rf"$\nu \in [{nu_min:.3f},{nu_max:.3f}]$"
    )

    fig.text(
        0.5, -0.02,
        training_text,
        ha="center",
        va="top",
        fontsize=13
    )

    combined_path = os.path.join(FIG_DIR, "multicase_fields.png")
    fig.savefig(combined_path, dpi=300, bbox_inches="tight")
    plt.close(fig)

    print(f"\nSaved combined figure → {combined_path}")

    print("\nSummary of relative L2 errors:")
    for row in summary:
        print(f"  {row['name']:25s}: {row['rel_l2']:.3e}")


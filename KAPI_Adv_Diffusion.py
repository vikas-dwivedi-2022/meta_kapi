# Cell 4: Sampling utilities for meta-training over (a,nu) WITH ν-range option
# ------------------------------

def sample_interior_points_meta(N_int, nu_range=None):
    """
    Interior points in [0,1] x [0,0.5] x [a_min,a_max] x [nu_low, nu_high].
    If nu_range is None, uses global (nu_min, nu_max).
    """
    if nu_range is None:
        nu_low, nu_high = nu_min, nu_max
    else:
        nu_low, nu_high = nu_range

    x  = x_min + (x_max - x_min) * torch.rand(N_int, 1)
    t  = t_min + (t_max - t_min) * torch.rand(N_int, 1)
    a  = a_min + (a_max - a_min) * torch.rand(N_int, 1)
    nu = nu_low + (nu_high - nu_low) * torch.rand(N_int, 1)

    return torch.cat([x, t, a, nu], dim=1).to(device)


def sample_ic_points_meta(N_ic, nu_range=None):
    """
    Initial condition points at t=0, with a ∈ [a_min,a_max], nu ∈ [nu_low, nu_high].
    """
    if nu_range is None:
        nu_low, nu_high = nu_min, nu_max
    else:
        nu_low, nu_high = nu_range

    x  = x_min + (x_max - x_min) * torch.rand(N_ic, 1)
    t  = torch.zeros(N_ic, 1)
    a  = a_min + (a_max - a_min) * torch.rand(N_ic, 1)
    nu = nu_low + (nu_high - nu_low) * torch.rand(N_ic, 1)

    return torch.cat([x, t, a, nu], dim=1).to(device)


def sample_bc_points_meta(N_bc, nu_range=None):
    """
    Boundary points at x=0 and x=1, t ∈ [0,0.5], a ∈ [a_min,a_max],
    nu ∈ [nu_low, nu_high].
    Returns xtanu_left, xtanu_right.
    """
    if nu_range is None:
        nu_low, nu_high = nu_min, nu_max
    else:
        nu_low, nu_high = nu_range

    t  = t_min + (t_max - t_min) * torch.rand(N_bc, 1)
    a  = a_min + (a_max - a_min) * torch.rand(N_bc, 1)
    nu = nu_low + (nu_high - nu_low) * torch.rand(N_bc, 1)

    xL = x_min * torch.ones(N_bc, 1)
    xR = x_max * torch.ones(N_bc, 1)

    xtanu_left  = torch.cat([xL, t, a, nu], dim=1)
    xtanu_right = torch.cat([xR, t, a, nu], dim=1)

    return xtanu_left.to(device), xtanu_right.to(device)

# ------------------------------
# Cell 5: PDE residual for meta advection–diffusion
#   u_t + a u_x - nu u_xx = 0
# ------------------------------

def advecdiff_residual_meta(model, xtanu_int):
    """
    xtanu_int: (N,4) interior points (x,t,a,nu).
    Returns residual r(x,t,a,nu) as (N,) tensor.
    """
    xtanu_int.requires_grad_(True)
    u = model(xtanu_int)  # (N,)

    grads = torch.autograd.grad(
        u,
        xtanu_int,
        grad_outputs=torch.ones_like(u),
        create_graph=True
    )[0]  # (N,4): [du/dx, du/dt, du/da, du/dnu]
    u_x = grads[:, 0]
    u_t = grads[:, 1]

    grads_x = torch.autograd.grad(
        u_x,
        xtanu_int,
        grad_outputs=torch.ones_like(u_x),
        create_graph=True
    )[0]
    u_xx = grads_x[:, 0]

    a_local  = xtanu_int[:, 2]
    nu_local = xtanu_int[:, 3]

    r = u_t + a_local * u_x - nu_local * u_xx
    return r

# ------------------------------
# Cell 6: Training loop with ν-curriculum + total-loss history + best checkpoint
# ------------------------------

def train_advecdiff_meta(
    N_int=2048,
    N_ic=512,
    N_bc=512,
    epochs=3000,
    lr=1e-3,
    M=100
):
    model = MetaDynamicKAPIRBF_AdvecDiff(M=M).to(device)
    optimizer = optim.Adam(model.parameters(), lr=lr)

    best_loss = float("inf")
    best_state = None

    history = {
        "epoch": [],
        "total_loss": []
    }

    for ep in range(1, epochs + 1):
        optimizer.zero_grad()

        # --------------------------
        # ν-curriculum (two phases)
        # --------------------------
        if ep <= epochs // 2:
            # Easy phase: only smoother problems (larger ν)
            nu_min_curr, nu_max_curr = nu_easy, nu_max
        else:
            # Full phase: entire training ν-range
            nu_min_curr, nu_max_curr = nu_min, nu_max

        # Sample interior / IC / BC points with current ν-range
        xtanu_int = sample_interior_points_meta(N_int, nu_range=(nu_min_curr, nu_max_curr))
        xtanu_ic  = sample_ic_points_meta(N_ic,       nu_range=(nu_min_curr, nu_max_curr))
        xtanu_L, xtanu_R = sample_bc_points_meta(N_bc, nu_range=(nu_min_curr, nu_max_curr))

        # ----- PDE loss -----
        r_int    = advecdiff_residual_meta(model, xtanu_int)
        loss_pde = torch.mean(r_int**2)

        # ----- IC loss (t=0, exact solution) -----
        u_ic   = model(xtanu_ic)
        x_ic   = xtanu_ic[:, 0:1]
        t_ic   = xtanu_ic[:, 1:2]
        a_ic   = xtanu_ic[:, 2:3]
        nu_ic  = xtanu_ic[:, 3:4]
        u0     = exact_advecdiff_torch(x_ic, t_ic, a_ic, nu_ic)
        loss_ic = torch.mean((u_ic - u0.squeeze())**2)

        # ----- BC loss at x=0 and x=1 (using exact solution) -----
        uL   = model(xtanu_L)
        xL   = xtanu_L[:, 0:1]
        tL   = xtanu_L[:, 1:2]
        aL   = xtanu_L[:, 2:3]
        nuL  = xtanu_L[:, 3:4]
        uL_exact = exact_advecdiff_torch(xL, tL, aL, nuL)

        uR   = model(xtanu_R)
        xR   = xtanu_R[:, 0:1]
        tR   = xtanu_R[:, 1:2]
        aR   = xtanu_R[:, 2:3]
        nuR  = xtanu_R[:, 3:4]
        uR_exact = exact_advecdiff_torch(xR, tR, aR, nuR)

        loss_bc = torch.mean((uL - uL_exact.squeeze())**2) + \
                  torch.mean((uR - uR_exact.squeeze())**2)

        # ----- Sparsity on gates -----
        g = torch.sigmoid(model.g_logits)
        loss_sparse = 1e-4 * torch.mean(torch.abs(g))

        # Total loss (same as before)
        loss = loss_pde + loss_ic + loss_bc + loss_sparse
        loss.backward()
        optimizer.step()

        # History
        history["epoch"].append(ep)
        history["total_loss"].append(loss.item())

        # Best checkpoint
        if loss.item() < best_loss:
            best_loss = loss.item()
            best_state = model.state_dict()

        if ep % 500 == 0:
            print(
                f"Epoch {ep:5d} | Total: {loss.item():.3e} | "
                f"PDE: {loss_pde.item():.3e}, IC: {loss_ic.item():.3e}, "
                f"BC: {loss_bc.item():.3e}, "
                f"nu_range=[{nu_min_curr:.3f}, {nu_max_curr:.3f}]"
            )

    print(f"\nBest training loss = {best_loss:.3e}")
    if best_state is not None:
        model.load_state_dict(best_state)

    return model, history

